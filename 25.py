n = int(input("Введите год нашей эры: "))

animals = ["Крыса", "Корова", "Тигр", "Заяц", "Дракон", "Змея",
           "Лошадь", "Овца", "Обезьяна", "Петух", "Собака", "Свинья"]

colors = ["Зеленый", "Красный", "Желтый", "Белый", "Черный"]

offset = n - 1984
animal = offset % 12
color = (offset // 2) % 5

print(f"{animals[animal]}, {colors[color]}")