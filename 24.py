a = float(input("Введите первое число: "))
b = float(input("Введите второе число: "))

if b >= 0:
    if (b ** 0.5) < a:
        b = b * 5
        print(f"Квадратный корень из второго числа меньше первого")
        print(f"Второе число увеличено в 5 раз: {b}")
    else:
        print(f"Квадратный корень из второго числа не меньше первого")
        print(f"Второе число осталось без изменений: {b}")
else:
    print("Из отрицательного числа нельзя извлечь квадратный корень")